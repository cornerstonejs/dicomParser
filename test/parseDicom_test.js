  function convertToByteArray(jsArray) {
    const byteArray = new Uint8Array(jsArray.length);
    let index = 0;
    
    for (let i = 0; i < jsArray.length; i++) {
        byteArray[index++] = jsArray[i];
    }

    return byteArray;
  }
  
